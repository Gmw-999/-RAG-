def call_deepseek(prompt: str, max_tokens: int = 4096) -> str:
    """调用 DeepSeek API"""
    headers = {
        "Authorization": f"Bearer {API_KEY}",
        "Content-Type": "application/json",
    }
    payload = {
        "model": "deepseek-chat",
        "messages": [{"role": "user", "content": prompt}],
        "temperature": 0.3,
        "max_tokens": max_tokens,
    }
    resp = requests.post(API_URL, json=payload, headers=headers, timeout=120)
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"]


def generate_batch(items):
    """批量生成一批条目"""
    item_list = "\n".join([f"- {name} ({ptype})" for name, ptype, cat in items])

    prompt = f"""你是资深农业植保专家。请为以下 {len(items)} 种农作物病虫害/虫害，每条生成一份完整的技术文档。

每条严格按照以下格式输出，用"---"分隔每条：

---
【名称】病虫害名称
【类型】病害 或 虫害
【概述】一句话概述，包括危害严重程度（50-80字）
【症状】详细描述田间症状表现，包括病斑/危害状的形态、颜色、大小、发生部位等（150-300字）
【病因】病原菌学名（病害）或害虫学名（虫害），发病/发生条件，传播途径，影响因素等（100-200字）
【预防】用① ② ③ ④ ⑤ 格式列出3-5条具体可操作的预防措施，包含品种选择、耕作管理、物理防治等
【治疗/防治】用① ② ③ ④ ⑤ 格式列出3-5条具体药剂防治方案，必须给出具体的药剂名称、稀释倍数或每亩用量
【推荐农药】JSON数组，格式：[{{"name":"农药名称","usage":"具体用法用量"}}]
【标签】逗号分隔的关键词，包括作物名、病虫害名、病原类型等

需要生成的清单：
{item_list}

要求：
1. 内容必须专业、准确，像农业技术手册一样详细
2. 农药名称必须是真实存在的农药产品
3. 防治方案必须具体可操作，不能泛泛而谈
4. 症状描述要细致到颜色、形状、大小、发生时期等
5. 直接输出内容，不要额外说明"""

    response = call_deepseek(prompt, max_tokens=4096)

    # 解析响应
    entries = []
    parts = response.split("---")
    for part in parts:
        part = part.strip()
        if not part or not part.startswith("【"):
            continue

        entry = {}
        lines = part.split("\n")
        current_key = None
        current_value = []

        for line in lines:
            line = line.strip()
            if not line:
                continue
            if line.startswith("【名称】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "title"
                current_value = [line.replace("【名称】", "").strip()]
            elif line.startswith("【类型】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "type"
                current_value = [line.replace("【类型】", "").strip()]
            elif line.startswith("【概述】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "summary"
                current_value = [line.replace("【概述】", "").strip()]
            elif line.startswith("【症状】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "symptoms"
                current_value = [line.replace("【症状】", "").strip()]
            elif line.startswith("【病因】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "cause"
                current_value = [line.replace("【病因】", "").strip()]
            elif line.startswith("【预防】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "prevention"
                current_value = [line.replace("【预防】", "").strip()]
            elif line.startswith("【治疗/防治】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "treatment"
                current_value = [line.replace("【治疗/防治】", "").strip()]
            elif line.startswith("【推荐农药】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "drugs"
                current_value = [line.replace("【推荐农药】", "").strip()]
            elif line.startswith("【标签】"):
                if current_key:
                    entry[current_key] = "\n".join(current_value).strip()
                current_key = "tags"
                current_value = [line.replace("【标签】", "").strip()]
            else:
                if current_key:
                    current_value.append(line)

        if current_key:
            entry[current_key] = "\n".join(current_value).strip()

        if entry.get("title"):
            entries.append(entry)

    return entries


def main():
    output_file = Path(__file__).parent / "knowledge_base.json"

    # 检查是否有已保存的进度
    all_entries = []
    completed = set()
    if output_file.exists():
        with open(output_file, 'r', encoding='utf-8') as f:
            all_entries = json.load(f)
        completed = {e["title"] for e in all_entries}
        print(f"已有 {len(all_entries)} 条，继续生成...")

    total = sum(len(v) for v in PEST_LIST.values())
    generated = len(all_entries)

    for category, pests in PEST_LIST.items():
        # 过滤掉已生成的
        to_generate = [(name, ptype) for name, ptype in pests if name not in completed]
        if not to_generate:
            print(f"[{category}] 已完成，跳过")
            continue

        # 每批 10 条
        batch_size = 10
        for i in range(0, len(to_generate), batch_size):
            batch = to_generate[i:i + batch_size]
            batch_with_cat = [(name, ptype, category) for name, ptype in batch]

            print(f"[{category}] 生成 {i+1}-{min(i+batch_size, len(to_generate))}/{len(to_generate)} ...", end=" ")

            try:
                entries = generate_batch(batch_with_cat)
                for entry in entries:
                    entry["category"] = category
                all_entries.extend(entries)

                # 每批保存
                with open(output_file, 'w', encoding='utf-8') as f:
                    json.dump(all_entries, f, ensure_ascii=False, indent=2)

                print(f"✓ 本批 {len(entries)} 条，累计 {len(all_entries)}/{total}")
                time.sleep(1)  # API 限流保护

            except Exception as e:
                print(f"✗ 失败: {e}")
                print("等待 5 秒后重试...")
                time.sleep(5)
                try:
                    entries = generate_batch(batch_with_cat)
                    for entry in entries:
                        entry["category"] = category
                    all_entries.extend(entries)
                    with open(output_file, 'w', encoding='utf-8') as f:
                        json.dump(all_entries, f, ensure_ascii=False, indent=2)
                    print(f"✓ 重试成功，累计 {len(all_entries)}/{total}")
                except Exception as e2:
                    print(f"✗ 重试仍失败: {e2}")

    print(f"\n===== 完成 =====")
    print(f"共生成 {len(all_entries)} 条知识")
    print(f"保存至: {output_file}")


if __name__ == "__main__":
    main()
